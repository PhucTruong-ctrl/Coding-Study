/*Bạn hãy viết chương trình tạo ra 2 biến a và b kiểu số nguyên,
sau đó gán giá trị cho a = 6, b = 2 và thực hiện hiển thị ra màn hình:
a + b = {P1}
a - b = {P2}
a * b = {P3}
a / b = {P4}
{P1} là tổng của a và b.
{P2} là hiệu của a và b.
{P3} là tích của a và b.
{P4} là thương của a và b.*/

#include <iostream>

using namespace std;

int main()
{
    int a = 6;
    int b = 2;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    return 0;
}
